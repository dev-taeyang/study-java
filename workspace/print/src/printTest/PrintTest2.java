package printTest;

public class PrintTest2 {
	public static void main(String[] args) {
		
		/*
		 * 제어 문자 
		 * 반드시 따옴표 안에서 작성한다.
		 * 
		 * 
		 * \n : new line, 줄바꿈, 개행문자
		 * \t : tab, 위아래 줄 간격 맞춰 띄기
		 * \" : " 표현
		 * \' : ' 표현
		 * \\ : \ 표현
		 */
		
		System.out.print("\"오태양\"\n");
		System.out.println("오태양");
		System.out.println("20살");
		System.out.print("\t20살\\");
		
	}

}
